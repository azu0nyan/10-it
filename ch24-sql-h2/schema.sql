create table USER (
  ID int not null generated by default as identity,
  LOGIN varchar(255) not null,
  PASSWORD varchar(255) not null,
  unique (ID),
  unique (LOGIN),
  primary key (ID)
);
create index USER_INDEX_0 on USER(LOGIN);
create table RECORD (
  ID int not null generated by default as identity,
  USERID int not null,
  LEADERBOARDID int not null,
  VALUE int not null,
  ACHEVEDAT timestamp,
  unique (ID),
  primary key (ID)
);
create index RECORD_INDEX_0 on RECORD(USERID);
create index RECORD_INDEX_1 on RECORD(LEADERBOARDID);
create table LEADERBOARD (
  ID int not null generated by default as identity,
  TITLE varchar(255) not null,
  DESCRIPTION varchar(255),
  ISACTIVE boolean not null,
  unique (ID),
  primary key (ID)
);
create index LEADERBOARD_INDEX_0 on LEADERBOARD(TITLE);
create table LIKES (
  ID int not null generated by default as identity,
  USERID int not null,
  RECORDID int not null,
  ISPOSITIVE boolean not null,
  unique (ID),
  primary key (ID)
);
create index LIKES_INDEX_0 on LIKES(RECORDID);
alter table RECORD add foreign key (USERID) references USER (ID) on delete no action on update no action;
alter table RECORD add foreign key (LEADERBOARDID) references LEADERBOARD (ID) on delete no action on update no action;
alter table LIKES add foreign key (USERID) references USER (ID) on delete no action on update no action;
alter table LIKES add foreign key (RECORDID) references RECORD (ID) on delete no action on update no action;